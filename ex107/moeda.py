def metade(num):
    metade = num / 2
    return metade


def dobro(num):
    dobro = num * 2
    return dobro


def aumentar(num, porcento):
    aumentar = ((porcento / 100) * num) + num
    return aumentar


def diminuir(num, porcento):
    diminuir = num - ((porcento / 100) * num)
    return diminuir
